    public void read(){
        
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            
            Connection conn = DB.getConnection();
            Statement stm = conn.createStatement();
            String sql = "SELECT * FROM daftarmenu ORDER BY total DESC";
            ResultSet rs = stm.executeQuery(sql);
            
            while(rs.next()) {
                
                Object[] data = new Object[4];
                data[0] = rs.getString("idMenu");
                data[1] = rs.getString("namaMenu");
                data[2] = rs.getString("hargaa");
                data[3] = rs.getString("total");
                
                model.addRow(data);
                
            }
            
            rs.close();
            stm.close();
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
        
    }
    
    public void tampil(){
        
        int row = tabelDaftarJualMenu.getRowCount();
        int totall;
        int totalBiaya = 0;
        
        for (int i = 0; i < row; i++) {
            
            totall = Integer.parseInt(tabelDaftarJualMenu.getValueAt(i, 3).toString());
            totalBiaya = totalBiaya + totall;
            
        }
        
        total.setText("Rp " + totalBiaya + ",00");
        
    }
    
    
